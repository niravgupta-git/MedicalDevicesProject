    private void createNetworkjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createNetworkjButton1ActionPerformed
        
        
        
        String networkName = txtNamejTextField1.getText();
        
        if(txtNamejTextField1.getText().isEmpty()==true || txtNamejTextField1.getText().startsWith(" ")) {
            JOptionPane.showMessageDialog(null, "Please enter valid text in field");
            return;
        }
        
        int count = 0;
        for (Network n : System.getNetworkList()){
            count++;
        } 
        
        if (count >= 1){
            JOptionPane.showMessageDialog(null, "Network already exists!!! Cannot  create duplicate Network !!!");
            return;
        }
        
            
        
        Network network = System.createAndAddNetwork();
        
        network.setName(networkName);
        populateNetworkTable();
    }//GEN-LAST:event_createNetworkjButton1ActionPerformed

