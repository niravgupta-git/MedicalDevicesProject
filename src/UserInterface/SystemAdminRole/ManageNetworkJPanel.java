    public void populateNetworkTable(){
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        for(Network network : System.getNetworkList()){
            Object[] row = new Object[4];
           row[0] = network.getName();
           dtm.addRow(row);
        }
        
    }

