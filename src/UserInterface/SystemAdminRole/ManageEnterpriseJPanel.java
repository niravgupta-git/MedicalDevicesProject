    private void CREATEjButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CREATEjButton1ActionPerformed
        Network network = (Network) networkjComboBox1.getSelectedItem();
        
        String name = enterprisenAMEjTextField1.getText();
        
        if(enterprisenAMEjTextField1.getText().isEmpty()==true || enterprisenAMEjTextField1.getText().startsWith(" ")) {
            JOptionPane.showMessageDialog(null, "Please enter valid text in enterprise name field");
            return;
        }
        
        Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) enterpriseTypejComboBox1.getSelectedItem();
        
        for (Network n : system.getNetworkList()){
            for (Enterprise e : n.getEnterpriseDirectory().getEnterpriseList()){
                if (e.getEnterpriseType().getValue().equalsIgnoreCase(type.getValue())){
                    if (type.getValue().equalsIgnoreCase(type.WareHouse.getValue())) {
                        System.out.println(e.getName());
                        JOptionPane.showMessageDialog(null, "Warehouse enterprise exists in the Network !!! Cannot create duplicate Enterprise !!!");
                        return;
                    } else if (type.getValue().equalsIgnoreCase(type.PHSenterprise.getValue())) {
                        System.out.println(e.getName());
                        JOptionPane.showMessageDialog(null, "PHS enterprise exists in the Network !!! Cannot create duplicate Enterprise !!!");
                        return;
                    }
                }
            }
        }
        
        for (Network n2 : system.getNetworkList()){
            for (Enterprise e2 : n2.getEnterpriseDirectory().getEnterpriseList()){
                if (e2.getName().equalsIgnoreCase(name)){
                    JOptionPane.showMessageDialog(null, "Same name enterprise exists !!! Cannot create duplicate Enterprise !!!");
                        return;
                }
            }
        }
        
        Enterprise enterprise = network.getEnterpriseDirectory().createAndAddEnterprise(name, type);
        populateEnterpriseTable();
        
    }//GEN-LAST:event_CREATEjButton1ActionPerformed

