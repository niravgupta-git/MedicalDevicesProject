    public void populateEnterpriseTable(){
        
        DefaultTableModel dtm = (DefaultTableModel) enterprisejTable1.getModel();
        dtm.setRowCount(0);
        for (Network network : system.getNetworkList()){
        for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
            Object[] row = new Object[3];
           row[0] = enterprise.getName();
           row[1] = network.getName();
           row[2] = enterprise.getEnterpriseType().getValue();
           dtm.addRow(row);
        }
        }
    }
    
    public void populateComboBox(){
        
        networkjComboBox1.removeAllItems();
        enterpriseTypejComboBox1.removeAllItems();
        
        for(Network network : system.getNetworkList()){
            networkjComboBox1.addItem(network);
        } 
        
        for(Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()){
            enterpriseTypejComboBox1.addItem(type);
        }
        
    }
